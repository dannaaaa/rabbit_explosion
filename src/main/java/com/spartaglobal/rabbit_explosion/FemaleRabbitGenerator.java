package com.spartaglobal.rabbit_explosion;

public class FemaleRabbitGenerator {

    public Rabbit createRabbit(int age, boolean mature, boolean alive, int timeOfBirth, boolean isPregnant, boolean giveBirth){
        FemaleRabbit rabbit = new FemaleRabbit();
        rabbit.setAge(age);
        rabbit.setMature(mature);
        rabbit.setAlive(alive);
        rabbit.setTimeOfBirth(timeOfBirth);
        rabbit.setIsPregnant(isPregnant);
        rabbit.setGiveBirth();





        return rabbit;
    }

}
